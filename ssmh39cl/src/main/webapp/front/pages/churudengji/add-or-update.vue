<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				dengjibianhao: this.getUUID(),
				yezhuzhanghao: '',
				yezhuxingming: '',
				lianxifangshi: '',
				shenfenzheng: '',
				tupian: '',
				churuleixing: '',
				churumude: '',
				sfsh: '',
				shhf: '',
				},
				yezhuzhanghaoOptions: [],
				yezhuzhanghaoIndex: 0,
				churuleixingOptions: [],
				churuleixingIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   dengjibianhao : false,
                                   yezhuzhanghao : false,
                                   yezhuxingming : false,
                                   lianxifangshi : false,
                                   shenfenzheng : false,
                                   tupian : false,
                                   churuleixing : false,
                                   churumude : false,
                                   sfsh : false,
                                   shhf : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},
			
			
			
			
			
			
			
			
			
			
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下2
			res = await this.$api.option(`yezhu`,`yezhuzhanghao`,{});
			this.yezhuzhanghaoOptions = res.data;
			// 自定义下拉框值
			this.churuleixingOptions = "出,入".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`churudengji`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='dengjibianhao'){
					this.ruleForm.dengjibianhao = obj[o];
					this.ro.dengjibianhao = true;
					continue;
					}
					if(o=='yezhuzhanghao'){
					this.ruleForm.yezhuzhanghao = obj[o];
					this.ro.yezhuzhanghao = true;
					continue;
					}
					if(o=='yezhuxingming'){
					this.ruleForm.yezhuxingming = obj[o];
					this.ro.yezhuxingming = true;
					continue;
					}
					if(o=='lianxifangshi'){
					this.ruleForm.lianxifangshi = obj[o];
					this.ro.lianxifangshi = true;
					continue;
					}
					if(o=='shenfenzheng'){
					this.ruleForm.shenfenzheng = obj[o];
					this.ro.shenfenzheng = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='churuleixing'){
					this.ruleForm.churuleixing = obj[o];
					this.ro.churuleixing = true;
					continue;
					}
					if(o=='churumude'){
					this.ruleForm.churumude = obj[o];
					this.ro.churumude = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
			// 下二随
			async yezhuzhanghaoChange (e) {
				this.yezhuzhanghaoIndex = e.target.value
				this.ruleForm.yezhuzhanghao = this.yezhuzhanghaoOptions[this.yezhuzhanghaoIndex]
				let res = await this.$api.follow(`yezhu`, `yezhuzhanghao`,{
					columnValue: this.ruleForm.yezhuzhanghao
				});
				if(res.data.yezhuxingming){
					this.ruleForm.yezhuxingming = res.data.yezhuxingming
				}
				if(res.data.lianxifangshi){
					this.ruleForm.lianxifangshi = res.data.lianxifangshi
				}
				if(res.data.shenfenzheng){
					this.ruleForm.shenfenzheng = res.data.shenfenzheng
				}
			},

			// 多级联动参数



			// 下拉变化
			churuleixingChange(e) {
				this.churuleixingIndex = e.target.value
				this.ruleForm.churuleixing = this.churuleixingOptions[this.churuleixingIndex]
			},

			tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {




















//跨表计算判断
				if(this.ruleForm.lianxifangshi&&(!this.$validate.isMobile(this.ruleForm.lianxifangshi))){
					this.$utils.msg(`联系方式应输入手机格式`);
					return
				}
				if(this.ruleForm.shenfenzheng&&(!this.$validate.checkIdCard(this.ruleForm.shenfenzheng))){
					this.$utils.msg(`身份证应输入身份证格式`);
					return
				}
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
						var obj = uni.getStorageSync('crossObj');
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`churudengji`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`churudengji`, this.ruleForm);
						}else{
							await this.$api.add(`churudengji`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`churudengji`, this.ruleForm);
					}else{
						await this.$api.add(`churudengji`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

